# ── 投入処理 ───────────────────────────────────────────────

def run():
    db = SessionLocal()
    try:
        # ── 管理者 ──
        admin = db.query(models.Admin).filter(models.Admin.login_id == "admin").first()
        if not admin:
            db.add(models.Admin(
                login_id="admin",
                password_hash=hash_password("Admin1234"),
            ))
            db.commit()
            print("✓ 管理者登録完了")
        else:
            print("- 管理者は既に存在します")

        # ── NGワード ──
        for word in NG_WORDS:
            exists = db.query(models.NGWord).filter(models.NGWord.word == word).first()
            if not exists:
                db.add(models.NGWord(word=word, created_at=rand_dt(days_max=3)))
        db.commit()
        print(f"✓ NGワード登録完了: {NG_WORDS}")

        # ── スレッド＆コメント ──
        for idx, title in enumerate(THREAD_TITLES):
            exists = db.query(models.Thread).filter(models.Thread.title == title).first()
            if exists:
                print(f"- スキップ（既存）: {title[:30]}...")
                continue

            thread_created = rand_dt(days_min=2, days_max=7)
            thread = models.Thread(
                title=title,
                created_at=thread_created,
                updated_at=thread_created,
            )
            db.add(thread)
            db.flush()

            comments_data = list(COMMENT_SETS[idx])

            # あぼーん確認用NGワードコメントを3スレに1件追加
            if idx % 3 == 0:
                insert_pos = random.randint(3, len(comments_data) - 1)
                abone_text = ABONE_COMMENTS[idx % len(ABONE_COMMENTS)]
                comments_data.insert(insert_pos, ("", "sage", abone_text))

            last_dt = thread_created
            for num, (raw_name, email, content) in enumerate(comments_data, start=1):
                # トリップ付きは3コメントに1件
                if num % 7 == 0 and TRIP_KEYS:
                    trip_key = TRIP_KEYS[num % len(TRIP_KEYS)]
                    handle = HANDLES[num % len(HANDLES)]
                    actual_name = handle
                    trip = generate_trip(trip_key)
                else:
                    actual_name = raw_name if raw_name else "名無しさん"
                    trip = ""

                session_id = SESSIONS[(idx * 20 + num) % len(SESSIONS)]
                date_str = last_dt.strftime("%Y-%m-%d")
                user_id = generate_user_id(session_id, date_str)

                interval = timedelta(minutes=random.randint(3, 40))
                last_dt = last_dt + interval

                comment = models.Comment(
                    thread_id=thread.id,
                    number=num,
                    name=actual_name,
                    email=email,
                    trip=trip,
                    content=content,
                    session_id=session_id,
                    user_id=user_id,
                    created_at=last_dt,
                )
                db.add(comment)

                if email.lower() != "sage":
                    thread.updated_at = last_dt

            db.commit()
            print(f"✓ スレッド作成: {title[:35]}... ({len(comments_data)}件)")

        print("\n✅ サンプルデータ投入完了")

    finally:
        db.close()


