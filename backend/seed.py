def jst_ago(**kwargs):
    return (NOW - timedelta(**kwargs))


def rand_dt(days_min=0, days_max=7, hours_min=0, hours_max=23):
    d = random.randint(days_min, days_max)
    h = random.randint(hours_min, hours_max)
    m = random.randint(0, 59)
    s = random.randint(0, 59)
    return NOW - timedelta(days=d, hours=h, minutes=m, seconds=s)


def generate_trip(key: str) -> str:
    return "◆" + hashlib.sha256(key.encode()).hexdigest()[:10]


def generate_user_id(session_id: str, date_str: str) -> str:
    return hashlib.sha256(f"{session_id}{date_str}".encode()).hexdigest()[:8]


def hash_password(password: str) -> str:
    return bcrypt.hashpw(password.encode(), bcrypt.gensalt()).decode()


